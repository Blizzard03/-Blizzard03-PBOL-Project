public class UsersModels {
    private String Nama_User, Email_User, Alamat_User, Nomor_Telepon_User, Password;
    private int ID_User;

    public String getNama_User() {
        return Nama_User;
    }

    public void setNama_User(String Nama_User) {
        this.Nama_User = Nama_User;
    }

    public String getEmail_User() {
        return Email_User;
    }

    public void setEmail_User(String Email_User) {
        this.Email_User = Email_User;
    }

    public String getAlamat_User() {
        return Alamat_User;
    }

    public void setAlamat_User(String Alamat_User) {
        this.Alamat_User = Alamat_User;
    }

    public String getNomor_Telepon_User() {
        return Nomor_Telepon_User;
    }

    public void setNomor_Telepon_User(String Nomor_Telepon_User) {
        this.Nomor_Telepon_User = Nomor_Telepon_User;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String Password) {
        this.Password = Password;
    }

    public int getID_User() {
        return ID_User;
    }

    public void setID_User(int ID_User) {
        this.ID_User = ID_User;
    }


}
